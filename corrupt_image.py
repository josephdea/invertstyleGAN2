def make_kernel(k):
    k = torch.tensor(k, dtype=torch.float32)

    if k.ndim == 1:
        k = k[None, :] * k[:, None]

    k /= k.sum()

    return k



class RandomizedInpainting(nn.Module):
    def __init__(self,perc = 0.5):
        self.perc = perc

    def forward(self,input):
        pct = 1-self.perc
        mask = np.random.binomial(1,pct, (256,256)).repeat(4,axis=0).repeat(4,axis=1)

        self.inpaint_mask = mask
        self.corrupted_img = np.einsum('ijk, ijk -> ijk', self.input, mask[..., np.newaxis])
        
        return self.corrupted_img

